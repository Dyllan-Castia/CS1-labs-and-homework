// This program prints to the screen the words: 
// PI = 3.14 
// Radius = 4 
// Circumference = 25.12 
#include <iostream> 
using namespace std; 
const double PI = 3.14; 
int main() 
{ 
float radius; 
radius = 4.0; 
cout << "PI = " << PI << endl; 
cout << "Radius = " << radius << endl; 
cout << "Circumference = " << 2 * PI * radius << endl; 
return 0; 
} 
/*program output
PI = 3.14
Radius = 4
Circumference = 25.12
*/
